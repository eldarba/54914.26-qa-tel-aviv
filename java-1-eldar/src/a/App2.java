package a;

public class App2 {

	public static void main(String[] args) {
		
		// data types
		System.out.println(5); // integer
		System.out.println(5F); // decimal (float)
		
		// variables
		// a numeral of type int
		int a = 25;
		System.out.println(a);
		
		// a numeral of type float
		float b = 25.32F;
		System.out.println(b);

	}
}
